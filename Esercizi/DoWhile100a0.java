package Esercizi;

public class DoWhile100a0 {
    public static void main(String[] args) {
        int num = 0;
        while(num <= 100){
            System.out.println(num);
            ++num;
        } do {
            num--;
            System.out.println(num +"A");
            
        } while(num >=1);
            System.out.println("ciclo finito");
            
        
    }
}
